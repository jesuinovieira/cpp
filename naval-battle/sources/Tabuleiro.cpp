#include "../header/Tabuleiro.hpp"


//_________________________________________________________________________________________________________________________________

Tabuleiro::Tabuleiro(char tmp)
{
	for(int x = 0; x < 15; x++)
		for(int y = 0; y < 15; y++)
			_tabuleiro[x][y] = tmp;
}
//_________________________________________________________________________________________________________________________________


//_________________________________________________________________________________________________________________________________

Tabuleiro::~Tabuleiro()
{
	//
}
//_________________________________________________________________________________________________________________________________


//_________________________________________________________________________________________________________________________________

char Tabuleiro::getXY(int x, int y) const
{
	return _tabuleiro[x][y];
}
//_________________________________________________________________________________________________________________________________


//_________________________________________________________________________________________________________________________________

void Tabuleiro::setXY(int x, int y, char marcador)
{
	_tabuleiro[x][y] = marcador;
}
//_________________________________________________________________________________________________________________________________


//_________________________________________________________________________________________________________________________________

void Tabuleiro::reiniciaTabuleiro(char tmp)
{
	for(int x = 0; x < 3; x++)
		for(int y = 0; y < 3; y++)
			setXY(x, y, tmp);
}
//_________________________________________________________________________________________________________________________________